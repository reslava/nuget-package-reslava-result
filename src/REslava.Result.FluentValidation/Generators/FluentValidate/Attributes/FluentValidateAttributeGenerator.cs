using Microsoft.CodeAnalysis.Text;
using System.Text;

namespace REslava.Result.FluentValidation.Generators.FluentValidate.Attributes
{
    internal static class FluentValidateAttributeGenerator
    {
        public static SourceText GenerateAttribute()
        {
            var source = @"// <auto-generated/>
using System;

namespace REslava.Result.FluentValidation
{
    /// <summary>
    /// Marks a class or record for FluentValidation bridge generation.
    /// Apply to types that have a registered <see cref=""FluentValidation.IValidator{T}""/> validator.
    /// The generated <c>Validate(IValidator&lt;T&gt; validator)</c> extension method runs the validator
    /// and returns <c>Result&lt;T&gt;</c> — integrates with SmartEndpoints auto-injection.
    /// </summary>
    /// <remarks>
    /// Users must separately install the <c>FluentValidation</c> NuGet package.
    /// Cannot be combined with <c>[Validate]</c> on the same type.
    /// </remarks>
    [AttributeUsage(AttributeTargets.Class | AttributeTargets.Struct, AllowMultiple = false)]
    public sealed class FluentValidateAttribute : Attribute { }
}";
            return SourceText.From(source, Encoding.UTF8);
        }
    }
}
