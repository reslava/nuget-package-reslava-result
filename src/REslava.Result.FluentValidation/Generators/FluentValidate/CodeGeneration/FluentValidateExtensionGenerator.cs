using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;
using System.Text;

namespace REslava.Result.FluentValidation.Generators.FluentValidate.CodeGeneration
{
    internal static class FluentValidateExtensionGenerator
    {
        /// <summary>
        /// Generates .Validate(IValidator&lt;T&gt;) and .ValidateAsync(IValidator&lt;T&gt;, CancellationToken)
        /// extension methods for the given type.
        /// </summary>
        public static SourceText GenerateForType(INamedTypeSymbol typeSymbol)
        {
            var typeName = typeSymbol.Name;
            var fullTypeName = typeSymbol.ToDisplayString();

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using System.Linq;");
            sb.AppendLine("using System.Threading;");
            sb.AppendLine("using System.Threading.Tasks;");
            sb.AppendLine("using FluentValidation;");
            sb.AppendLine("using REslava.Result;");
            sb.AppendLine();
            sb.AppendLine("namespace Generated.FluentValidationExtensions");
            sb.AppendLine("{");
            sb.AppendLine($"    public static class {typeName}FluentValidationExtensions");
            sb.AppendLine("    {");

            // Sync overload
            sb.AppendLine($"        public static Result<{fullTypeName}> Validate(");
            sb.AppendLine($"            this {fullTypeName} instance,");
            sb.AppendLine($"            IValidator<{fullTypeName}> validator)");
            sb.AppendLine("        {");
            sb.AppendLine("            var result = validator.Validate(instance);");
            sb.AppendLine("            if (result.IsValid)");
            sb.AppendLine($"                return Result<{fullTypeName}>.Ok(instance);");
            sb.AppendLine();
            sb.AppendLine("            var errors = result.Errors");
            sb.AppendLine("                .Select(e => (IError)ValidationError.Field(e.PropertyName, e.ErrorMessage))");
            sb.AppendLine("                .ToList();");
            sb.AppendLine($"            return Result<{fullTypeName}>.Fail(errors);");
            sb.AppendLine("        }");
            sb.AppendLine();

            // Async overload
            sb.AppendLine($"        public static async Task<Result<{fullTypeName}>> ValidateAsync(");
            sb.AppendLine($"            this {fullTypeName} instance,");
            sb.AppendLine($"            IValidator<{fullTypeName}> validator,");
            sb.AppendLine("            CancellationToken cancellationToken = default)");
            sb.AppendLine("        {");
            sb.AppendLine("            var result = await validator.ValidateAsync(instance, cancellationToken);");
            sb.AppendLine("            if (result.IsValid)");
            sb.AppendLine($"                return Result<{fullTypeName}>.Ok(instance);");
            sb.AppendLine();
            sb.AppendLine("            var errors = result.Errors");
            sb.AppendLine("                .Select(e => (IError)ValidationError.Field(e.PropertyName, e.ErrorMessage))");
            sb.AppendLine("                .ToList();");
            sb.AppendLine($"            return Result<{fullTypeName}>.Fail(errors);");
            sb.AppendLine("        }");

            sb.AppendLine("    }");
            sb.AppendLine("}");

            return SourceText.From(sb.ToString(), Encoding.UTF8);
        }
    }
}
