using Microsoft.CodeAnalysis.Text;
using System.Text;

namespace REslava.Result.SourceGenerators.Generators.Validate.Attributes
{
    internal class ValidateAttributeGenerator
    {
        public static SourceText GenerateAttribute()
        {
            var source = @"// <auto-generated/>
using System;

namespace REslava.Result.SourceGenerators
{
    /// <summary>
    /// Marks a class or record for automatic Validate() extension method generation.
    /// Apply to types decorated with System.ComponentModel.DataAnnotations attributes.
    /// The generated Validate() method returns Result&lt;T&gt; — use .ToIResult() or .ToActionResult() to map to HTTP.
    /// </summary>
    [AttributeUsage(AttributeTargets.Class | AttributeTargets.Struct, AllowMultiple = false)]
    public sealed class ValidateAttribute : Attribute { }
}";
            return SourceText.From(source, Encoding.UTF8);
        }
    }
}
