using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;
using System.Text;

namespace REslava.Result.SourceGenerators.Generators.Validate.CodeGeneration
{
    internal class ValidateExtensionGenerator
    {
        public static SourceText GenerateForType(INamedTypeSymbol typeSymbol)
        {
            var typeName = typeSymbol.Name;
            var fullTypeName = typeSymbol.ToDisplayString();

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using System.ComponentModel.DataAnnotations;");
            sb.AppendLine("using System.Linq;");
            sb.AppendLine("using REslava.Result;");
            sb.AppendLine();
            sb.AppendLine("namespace Generated.ValidationExtensions");
            sb.AppendLine("{");
            sb.AppendLine($"    public static class {typeName}ValidationExtensions");
            sb.AppendLine("    {");
            sb.AppendLine($"        public static Result<{fullTypeName}> Validate(this {fullTypeName} instance)");
            sb.AppendLine("        {");
            sb.AppendLine("            var context = new ValidationContext(instance);");
            sb.AppendLine("            var results = new List<ValidationResult>();");
            sb.AppendLine("            if (Validator.TryValidateObject(instance, context, results, validateAllProperties: true))");
            sb.AppendLine($"                return Result<{fullTypeName}>.Ok(instance);");
            sb.AppendLine();
            sb.AppendLine("            var errors = results");
            sb.AppendLine("                .Select(r =>");
            sb.AppendLine("                {");
            sb.AppendLine("                    var fieldName = r.MemberNames.FirstOrDefault();");
            sb.AppendLine("                    return string.IsNullOrEmpty(fieldName)");
            sb.AppendLine("                        ? (IError)new ValidationError(r.ErrorMessage ?? \"Validation failed\")");
            sb.AppendLine("                        : (IError)new ValidationError(fieldName, r.ErrorMessage ?? \"Validation failed\");");
            sb.AppendLine("                })");
            sb.AppendLine("                .ToList();");
            sb.AppendLine($"            return Result<{fullTypeName}>.Fail(errors);");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            return SourceText.From(sb.ToString(), Encoding.UTF8);
        }
    }
}
