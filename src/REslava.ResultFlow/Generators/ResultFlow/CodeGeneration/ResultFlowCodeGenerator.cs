using Microsoft.CodeAnalysis.Text;
using System.Collections.Generic;
using System.Text;

namespace REslava.ResultFlow.Generators.ResultFlow.CodeGeneration
{
    internal static class ResultFlowCodeGenerator
    {
        /// <summary>
        /// Generates a <c>.cs</c> file with one <c>internal static class {className}_Flows</c>
        /// containing constants per diagram: <c>{methodName}</c>, and — when layer data is
        /// available — <c>{methodName}_LayerView</c>, <c>{methodName}_Stats</c>,
        /// and <c>{methodName}_ErrorSurface</c>.
        /// </summary>
        public static SourceText Generate(
            string className,
            IReadOnlyList<(string methodName, string mermaid, string? layerView, string? stats, string? errorSurface, string? typeFlow)> diagrams)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// REslava.Result.Flow — auto-generated pipeline diagrams");
            sb.AppendLine("// Do not edit manually.");
            sb.AppendLine();
            sb.AppendLine("namespace Generated.ResultFlow");
            sb.AppendLine("{");
            sb.AppendLine($"    internal static class {className}_Flows");
            sb.AppendLine("    {");

            // ── Legend — emitted once per class ──────────────────────────────
            var legendMermaid = BuildLegendMermaid();
            var escapedLegend = legendMermaid.Replace("\"", "\"\"");
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// Mermaid legend for all node types used in ResultFlow diagrams.");
            sb.AppendLine("        /// Paste into any Mermaid renderer to see the color/shape reference.");
            sb.AppendLine("        /// </summary>");
            sb.Append($"        public const string Legend = @\"{escapedLegend.Trim()}");
            sb.AppendLine("\";");
            sb.AppendLine();

            foreach (var (methodName, mermaid, layerView, stats, errorSurface, typeFlow) in diagrams)
            {
                // ── Pipeline diagram ─────────────────────────────────────────
                var escapedMermaid = mermaid.Replace("\"", "\"\"");
                sb.AppendLine($"        /// <summary>");
                sb.AppendLine($"        /// Mermaid flow diagram for {methodName}.");
                sb.AppendLine($"        /// Copy into any Mermaid renderer to visualize the pipeline.");
                sb.AppendLine($"        /// </summary>");
                sb.Append($"        public const string {methodName} = @\"{escapedMermaid.Trim()}");
                sb.AppendLine("\";");
                sb.AppendLine();

                // ── TypeFlow diagram (type-labelled edges) ────────────────────
                if (typeFlow != null)
                {
                    var escapedTypeFlow = typeFlow.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Type-annotated flow diagram for {methodName}.");
                    sb.AppendLine($"        /// Same nodes as {methodName} but success edges carry the Result&lt;T&gt; type.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_TypeFlow = @\"{escapedTypeFlow.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }

                // ── LayerView diagram (optional) ─────────────────────────────
                if (layerView != null)
                {
                    var escapedLayerView = layerView.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Architecture-level diagram for {methodName}.");
                    sb.AppendLine($"        /// Shows one node per traced method, grouped by architectural layer and class.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_LayerView = @\"{escapedLayerView.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }

                // ── Stats markdown table (optional — emitted alongside LayerView) ──
                if (stats != null)
                {
                    var escapedStats = stats.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Pipeline statistics for {methodName} — markdown table.");
                    sb.AppendLine($"        /// Paste into docs, changelogs, or ADRs.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_Stats = @\"{escapedStats.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }

                // ── ErrorSurface diagram (optional — emitted alongside LayerView) ──
                if (errorSurface != null)
                {
                    var escapedErrorSurface = errorSurface.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Fail-edges-only view for {methodName}.");
                    sb.AppendLine($"        /// Shows every error type the pipeline can produce and where it originates.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_ErrorSurface = @\"{escapedErrorSurface.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");

            return SourceText.From(sb.ToString(), Encoding.UTF8);
        }

        private static string BuildLegendMermaid()
        {
            var sb = new StringBuilder();
            sb.AppendLine("flowchart LR");
            // S[ ]:::entry is invisible; ==> shows the thick entry arrow into Entry node.
            // ~~~ links are invisible (no line/arrow) and force horizontal LR placement.
            sb.AppendLine("    S[ ]:::entry ==> E[\"Entry\"]:::operation");
            sb.AppendLine("    E ~~~ B[\"Bind\"]:::bind");
            sb.AppendLine("    B ~~~ M[\"Map\"]:::map");
            sb.AppendLine("    M ~~~ G[\"<span title='hover shows condition'>Guard</span>\"]:::gatekeeper");
            sb.AppendLine("    G ~~~ T[\"Tap\"]:::sideeffect");
            sb.AppendLine("    T ~~~ X{{\"Match\"}}:::terminal");
            sb.AppendLine("    X ~~~ SUC([success]):::success");
            sb.AppendLine("    X ~~~ F([fail]):::failure");
            sb.AppendLine("    F ~~~ A[\"\u26a1 = async \u00b7 Guard: condition on hover<br/>FAIL: 1\u20133 errors inline \u00b7 4+ as \u2139\uFE0F tooltip\"]:::note");
            sb.AppendLine("    A ~~~ V[\"_Diagram \u00b7 _TypeFlow (edge type labels) \u00b7 _LayerView \u00b7 _Stats \u00b7 _ErrorSurface \u00b7 _ErrorPropagation\"]:::note");
            sb.AppendLine("    classDef entry      fill:none,stroke:none");
            sb.AppendLine("    classDef operation  fill:#fef0e3,color:#b86a1c");
            sb.AppendLine("    classDef bind       fill:#e3f0e8,color:#2f7a5c,stroke:#1a5c3c,stroke-width:3px");
            sb.AppendLine("    classDef map        fill:#e3f0e8,color:#2f7a5c");
            sb.AppendLine("    classDef gatekeeper fill:#e3e9fa,color:#3f5c9a");
            sb.AppendLine("    classDef sideeffect fill:#fff4d9,color:#b8882c");
            sb.AppendLine("    classDef terminal   fill:#f2e3f5,color:#8a4f9e");
            sb.AppendLine("    classDef success    fill:#e6f6ea,color:#1c7e4f");
            sb.AppendLine("    classDef failure    fill:#f8e3e3,color:#b13e3e");
            sb.AppendLine("    classDef note       fill:#f5f5f5,color:#555555,stroke:#cccccc");
            return sb.ToString().TrimEnd();
        }

    }
}
