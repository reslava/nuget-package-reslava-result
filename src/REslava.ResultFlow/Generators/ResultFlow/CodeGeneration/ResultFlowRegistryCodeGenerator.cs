using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;
using REslava.ResultFlow.Generators.ResultFlow.Models;
using REslava.ResultFlow.Generators.ResultFlow.Orchestration;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace REslava.ResultFlow.Generators.ResultFlow.CodeGeneration
{
    internal static class ResultFlowRegistryCodeGenerator
    {
        /// <summary>
        /// Generates <c>{ClassName}_PipelineRegistry.g.cs</c> with a <c>_Methods</c> JSON index
        /// and a <c>{MethodName}_Info</c> JSON constant per method.
        /// Syntax-only variant: <c>errorTypes</c> and <c>nodeKindFlags</c> are always empty arrays.
        /// <c>nodeCount</c> is derived from the syntax chain extractor when available.
        /// </summary>
        public static SourceText Generate(
            string className,
            IReadOnlyList<MethodRegistryModel> methods,
            Compilation compilation)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// REslava.ResultFlow — pipeline registry");
            sb.AppendLine("// Do not edit manually.");
            sb.AppendLine();
            sb.AppendLine("namespace Generated.ResultFlow");
            sb.AppendLine("{");
            sb.AppendLine($"    internal static class {className}_PipelineRegistry");
            sb.AppendLine("    {");

            // _Methods index
            var methodNames = string.Join(",", methods.Select(m => $"\\\"{m.MethodName}\\\""));
            sb.AppendLine($"        public const string _Methods = \"[{methodNames}]\";");
            sb.AppendLine();

            foreach (var method in methods)
            {
                string infoJson;
                if (method.HasDiagram && method.Syntax != null)
                {
                    // Run syntax chain extractor for nodeCount
                    var semanticModel = compilation.GetSemanticModel(method.Syntax.SyntaxTree);
                    var chain = ResultFlowChainExtractor.Extract(
                        method.Syntax,
                        semanticModel,
                        customMappings: null,
                        maxDepth: method.MaxDepth,
                        compilation: compilation);

                    var nodeCount = chain != null
                        ? chain.Count(n => n.Kind != NodeKind.Invisible)
                        : 0;

                    infoJson = BuildFullInfo(method, nodeCount);
                }
                else
                {
                    infoJson = BuildMinimalInfo(method);
                }

                var escaped = infoJson.Replace("\\", "\\\\").Replace("\"", "\\\"");
                sb.AppendLine($"        /// <summary>Pipeline registry info for {method.MethodName}.</summary>");
                sb.AppendLine($"        public const string {method.MethodName}_Info = \"{escaped.Trim()}\";");
                sb.AppendLine();
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");

            return SourceText.From(sb.ToString(), Encoding.UTF8);
        }

        // ── JSON builders ─────────────────────────────────────────────────────

        private static string BuildFullInfo(MethodRegistryModel method, int nodeCount)
        {
            return
                $"{{\"sourceLine\":{method.SourceLine}," +
                $"\"returnType\":\"{method.ReturnType}\"," +
                $"\"returnTypeFullName\":\"{EscapeJson(method.ReturnTypeFullName)}\"," +
                $"\"isAsync\":{BoolStr(method.IsAsync)}," +
                $"\"hasDiagram\":true," +
                $"\"nodeCount\":{nodeCount}," +
                $"\"errorTypes\":[]," +
                $"\"nodeKindFlags\":[]}}";
        }

        private static string BuildMinimalInfo(MethodRegistryModel method)
        {
            return
                $"{{\"sourceLine\":{method.SourceLine}," +
                $"\"returnType\":\"{method.ReturnType}\"," +
                $"\"returnTypeFullName\":\"{EscapeJson(method.ReturnTypeFullName)}\"," +
                $"\"isAsync\":{BoolStr(method.IsAsync)}," +
                $"\"hasDiagram\":false}}";
        }

        // ── Helpers ───────────────────────────────────────────────────────────

        private static string BoolStr(bool value) => value ? "true" : "false";

        private static string EscapeJson(string value) =>
            value.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }
}
