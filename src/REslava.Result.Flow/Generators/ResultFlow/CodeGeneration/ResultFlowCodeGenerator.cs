using Microsoft.CodeAnalysis.Text;
using System.Collections.Generic;
using System.Text;

namespace REslava.Result.Flow.Generators.ResultFlow.CodeGeneration
{
    internal static class ResultFlowCodeGenerator
    {
        /// <summary>
        /// Generates a <c>.cs</c> file with one <c>internal static class {className}_Flows</c>
        /// containing one <c>public const string {methodName}</c> per diagram.
        /// </summary>
        public static SourceText Generate(string className, IReadOnlyList<(string methodName, string mermaid)> diagrams)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// REslava.Result.Flow — auto-generated pipeline diagrams with type travel");
            sb.AppendLine("// Do not edit manually.");
            sb.AppendLine();
            sb.AppendLine("namespace Generated.ResultFlow");
            sb.AppendLine("{");
            sb.AppendLine($"    internal static class {className}_Flows");
            sb.AppendLine("    {");

            foreach (var (methodName, mermaid) in diagrams)
            {
                var escapedMermaid = mermaid.Replace("\"", "\"\"");

                sb.AppendLine($"        /// <summary>");
                sb.AppendLine($"        /// Mermaid flow diagram for {methodName}.");
                sb.AppendLine($"        /// Copy into any Mermaid renderer to visualize the pipeline.");
                sb.AppendLine($"        /// </summary>");
                sb.AppendLine($"        public const string {methodName} = @\"");
                sb.AppendLine(escapedMermaid);
                sb.AppendLine($"\";");
                sb.AppendLine();

                // Sidecar markdown — wraps the Mermaid diagram in a .md-ready code block.
                // Write to disk with: File.WriteAllText("Pipeline.md", {className}_Flows.{methodName}_Sidecar)
                var sidecar = BuildSidecarMarkdown(methodName, mermaid);
                var escapedSidecar = sidecar.Replace("\"", "\"\"");
                sb.AppendLine($"        /// <summary>");
                sb.AppendLine($"        /// Markdown sidecar for {methodName} — Mermaid diagram wrapped in a code block.");
                sb.AppendLine($"        /// Previewable in VS Code (Ctrl+Shift+V) or rendered on GitHub.");
                sb.AppendLine($"        /// Write to disk: File.WriteAllText(\"{methodName}.ResultFlow.md\", {methodName}_Sidecar)");
                sb.AppendLine($"        /// </summary>");
                sb.AppendLine($"        public const string {methodName}_Sidecar = @\"");
                sb.AppendLine(escapedSidecar);
                sb.AppendLine($"\";");
                sb.AppendLine();
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");

            return SourceText.From(sb.ToString(), Encoding.UTF8);
        }

        private static string BuildSidecarMarkdown(string methodName, string mermaid)
        {
            var md = new StringBuilder();
            md.AppendLine($"# Pipeline — {methodName}");
            md.AppendLine();
            md.AppendLine("```mermaid");
            md.AppendLine(mermaid);
            md.AppendLine("```");
            return md.ToString().TrimEnd();
        }
    }
}
