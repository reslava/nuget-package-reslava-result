using Microsoft.CodeAnalysis.Text;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace REslava.Result.Flow.Generators.ResultFlow.CodeGeneration
{
    /// <summary>Data needed to emit a <c>FlowProxy</c> partial class addition.</summary>
    internal sealed class FlowProxyMethodInfo
    {
        internal string MethodName = "";
        /// <summary>Fully qualified containing type, e.g. <c>global::MyApp.OrderService</c>.</summary>
        internal string ContainingTypeFqn = "";
        /// <summary>Short (unqualified) containing type name, e.g. <c>OrderService</c>.</summary>
        internal string ContainingTypeShortName = "";
        /// <summary>Containing namespace, e.g. <c>MyApp.Services</c>. Empty string for global namespace.</summary>
        internal string ContainingNamespace = "";
        /// <summary>Access modifier keyword for the containing type: <c>"public"</c>, <c>"internal"</c>, etc.</summary>
        internal string ContainingTypeAccessibility = "public";
        /// <summary>True when the containing type is a static class.</summary>
        internal bool IsStatic;
        /// <summary>Fully qualified return type (may be Task-wrapped), e.g. <c>global::REslava.Result.Result&lt;...&gt;</c>.</summary>
        internal string ReturnTypeFqn = "";
        /// <summary>True when the return type is <c>Task&lt;Result&lt;T&gt;&gt;</c>.</summary>
        internal bool ReturnsTask;
        /// <summary>True when the inner Result is generic (Result&lt;T&gt;) — enables <c>result.Value</c>.</summary>
        internal bool ResultIsGeneric;
        /// <summary>True when T in Result&lt;T&gt; is a value type — uses <c>.ToString()</c> instead of <c>?.ToString()</c>.</summary>
        internal bool ResultValueIsValueType;
        internal List<(string TypeFqn, string ParamName, bool IsValueType)> Parameters = new List<(string, string, bool)>();
        internal string PipelineId = "";
        internal string[] NodeIds = System.Array.Empty<string>();
    }

    internal static class ResultFlowCodeGenerator
    {
        /// <summary>
        /// Generates a <c>.cs</c> file with one <c>internal static class {className}_Flows</c>
        /// containing constants per diagram: <c>{methodName}</c>, and — when layer data is
        /// available — <c>{methodName}_LayerView</c>, <c>{methodName}_Stats</c>,
        /// <c>{methodName}_ErrorSurface</c>, and <c>{methodName}_ErrorPropagation</c>.
        /// When <paramref name="flowProxyMethods"/> is provided, also emits a
        /// <c>partial class {ClassName}</c> with nested <c>FlowProxy</c> (and optionally
        /// <c>DebugProxy</c>) in the user's namespace.
        /// </summary>
        public static SourceText Generate(
            string className,
            IReadOnlyList<(string methodName, string mermaid, string? layerView, string? stats, string? errorSurface, string? errorPropagation, string? typeFlow)> diagrams,
            IReadOnlyList<FlowProxyMethodInfo>? flowProxyMethods = null)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// REslava.Result.Flow — auto-generated pipeline diagrams with type travel");
            sb.AppendLine("// Do not edit manually.");
            sb.AppendLine();

            // ── Block 1: diagram constants (Generated.ResultFlow namespace) ──
            sb.AppendLine("namespace Generated.ResultFlow");
            sb.AppendLine("{");
            sb.AppendLine($"    internal static class {className}_Flows");
            sb.AppendLine("    {");

            // ── Legend — emitted once per class ──────────────────────────────
            var legendMermaid = BuildLegendMermaid();
            var escapedLegend = legendMermaid.Replace("\"", "\"\"");
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// Mermaid legend for all node types used in ResultFlow diagrams.");
            sb.AppendLine("        /// Paste into any Mermaid renderer to see the color/shape reference.");
            sb.AppendLine("        /// </summary>");
            sb.Append($"        public const string Legend = @\"{escapedLegend.Trim()}");
            sb.AppendLine("\";");
            sb.AppendLine();

            foreach (var (methodName, mermaid, layerView, stats, errorSurface, errorPropagation, typeFlow) in diagrams)
            {
                // ── Pipeline diagram ─────────────────────────────────────────
                var escapedMermaid = mermaid.Replace("\"", "\"\"");
                sb.AppendLine($"        /// <summary>");
                sb.AppendLine($"        /// Mermaid flow diagram for {methodName}.");
                sb.AppendLine($"        /// Copy into any Mermaid renderer to visualize the pipeline.");
                sb.AppendLine($"        /// </summary>");
                sb.Append($"        public const string {methodName} = @\"{escapedMermaid.Trim()}");
                sb.AppendLine("\";");
                sb.AppendLine();

                // ── TypeFlow diagram (type-labelled edges) ────────────────────
                if (typeFlow != null)
                {
                    var escapedTypeFlow = typeFlow.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Type-annotated flow diagram for {methodName}.");
                    sb.AppendLine($"        /// Same nodes as {methodName} but success edges carry the Result&lt;T&gt; type.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_TypeFlow = @\"{escapedTypeFlow.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }

                // ── LayerView diagram (optional) ─────────────────────────────
                if (layerView != null)
                {
                    var escapedLayerView = layerView.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Architecture-level diagram for {methodName}.");
                    sb.AppendLine($"        /// Shows one node per traced method, grouped by architectural layer and class.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_LayerView = @\"{escapedLayerView.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }

                // ── Stats markdown table (optional) ───────────────────────────
                if (stats != null)
                {
                    var escapedStats = stats.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Pipeline statistics for {methodName} — markdown table.");
                    sb.AppendLine($"        /// Paste into docs, changelogs, or ADRs.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_Stats = @\"{escapedStats.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }

                // ── ErrorSurface diagram (optional) ───────────────────────────
                if (errorSurface != null)
                {
                    var escapedErrorSurface = errorSurface.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Fail-edges-only view for {methodName}.");
                    sb.AppendLine($"        /// Shows every error type the pipeline can produce and where it originates.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_ErrorSurface = @\"{escapedErrorSurface.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }

                // ── ErrorPropagation diagram (optional) ───────────────────────
                if (errorPropagation != null)
                {
                    var escapedErrorPropagation = errorPropagation.Replace("\"", "\"\"");
                    sb.AppendLine($"        /// <summary>");
                    sb.AppendLine($"        /// Per-layer error propagation diagram for {methodName}.");
                    sb.AppendLine($"        /// Shows which error types can escape from each architectural layer.");
                    sb.AppendLine($"        /// </summary>");
                    sb.Append($"        public const string {methodName}_ErrorPropagation = @\"{escapedErrorPropagation.Trim()}");
                    sb.AppendLine("\";");
                    sb.AppendLine();
                }
            }

            sb.AppendLine("    }"); // close {className}_Flows
            sb.AppendLine("}");     // close Generated.ResultFlow namespace

            // ── Block 2: FlowProxy partial class (user's namespace) ───────────
            if (flowProxyMethods != null && flowProxyMethods.Count > 0)
                EmitFlowProxyBlock(sb, flowProxyMethods);

            return SourceText.From(sb.ToString(), Encoding.UTF8);
        }

        // ─────────────────────────────────────────────────────────────────────
        // FlowProxy emission
        // ─────────────────────────────────────────────────────────────────────

        private static void EmitFlowProxyBlock(StringBuilder sb, IReadOnlyList<FlowProxyMethodInfo> methods)
        {
            var first = methods[0];
            var ns = first.ContainingNamespace;
            var shortName = first.ContainingTypeShortName;
            var accessibility = first.ContainingTypeAccessibility;
            var isStatic = first.IsStatic;
            var hasNs = !string.IsNullOrEmpty(ns);
            // ci = class indent relative to namespace open-brace
            var ci = hasNs ? "    " : "";

            sb.AppendLine();
            if (hasNs)
            {
                sb.AppendLine($"namespace {ns}");
                sb.AppendLine("{");
            }

            var staticMod = isStatic ? "static " : "";
            sb.AppendLine($"{ci}{accessibility} {staticMod}partial class {shortName}");
            sb.AppendLine($"{ci}{{");

            // Flow property
            if (isStatic)
            {
                sb.AppendLine($"{ci}    private static readonly FlowProxy _flowProxy = new FlowProxy();");
                sb.AppendLine($"{ci}    public static FlowProxy Flow => _flowProxy;");
            }
            else
            {
                sb.AppendLine($"{ci}    private FlowProxy? _flow;");
                sb.AppendLine($"{ci}    public FlowProxy Flow => _flow ??= new FlowProxy(this);");
            }
            sb.AppendLine();

            // FlowProxy class
            sb.AppendLine($"{ci}    public sealed class FlowProxy");
            sb.AppendLine($"{ci}    {{");

            if (isStatic)
            {
                sb.AppendLine($"{ci}        internal FlowProxy() {{ }}");
            }
            else
            {
                sb.AppendLine($"{ci}        private readonly {shortName} _self;");
                sb.AppendLine($"{ci}        internal FlowProxy({shortName} self) {{ _self = self; }}");
            }
            sb.AppendLine();

            // _nodeIds_ static fields (one per method; shared with DebugProxy via outer class access)
            foreach (var tm in methods)
            {
                sb.AppendLine($"{ci}        private static readonly string[] _nodeIds_{tm.MethodName} = new string[]");
                sb.AppendLine($"{ci}        {{");
                foreach (var nodeId in tm.NodeIds)
                    sb.AppendLine($"{ci}            \"{EscapeStringLiteral(nodeId)}\",");
                sb.AppendLine($"{ci}        }};");
                sb.AppendLine();
            }

            // FlowProxy wrapper methods
            foreach (var tm in methods)
                EmitProxyMethod(sb, tm, $"{ci}        ", isDebug: false, shortName, isStatic);

            // DebugProxy — always emitted; uses buffer.Save() instead of PipelineTraceHost
            sb.AppendLine($"{ci}        private DebugProxy? _debug;");
            if (isStatic)
                sb.AppendLine($"{ci}        public DebugProxy Debug => _debug ??= new DebugProxy();");
            else
                sb.AppendLine($"{ci}        public DebugProxy Debug => _debug ??= new DebugProxy(_self);");
            sb.AppendLine();

            sb.AppendLine($"{ci}        public sealed class DebugProxy");
            sb.AppendLine($"{ci}        {{");

            if (isStatic)
            {
                sb.AppendLine($"{ci}            internal DebugProxy() {{ }}");
            }
            else
            {
                sb.AppendLine($"{ci}            private readonly {shortName} _self;");
                sb.AppendLine($"{ci}            internal DebugProxy({shortName} self) {{ _self = self; }}");
            }
            sb.AppendLine();

            foreach (var tm in methods)
                EmitProxyMethod(sb, tm, $"{ci}            ", isDebug: true, shortName, isStatic);

            sb.AppendLine($"{ci}        }}"); // close DebugProxy

            sb.AppendLine($"{ci}    }}"); // close FlowProxy
            sb.AppendLine($"{ci}}}");     // close partial class

            if (hasNs)
                sb.AppendLine("}"); // close namespace
        }

        private static void EmitProxyMethod(
            StringBuilder sb,
            FlowProxyMethodInfo tm,
            string indent,
            bool isDebug,
            string typeShortName,
            bool isStatic)
        {
            var asyncPrefix = tm.ReturnsTask ? "async " : "";
            var paramList = string.Join(", ", tm.Parameters.Select(p => $"{p.TypeFqn} {p.ParamName}"));

            sb.AppendLine($"{indent}public {asyncPrefix}{tm.ReturnTypeFqn} {tm.MethodName}({paramList})");
            sb.AppendLine($"{indent}{{");

            // First non-CancellationToken parameter value for BeginPipeline inputValue
            var firstValueParam = tm.Parameters.FirstOrDefault(p => !p.TypeFqn.Contains("CancellationToken"));
            var inputValueExpr = firstValueParam.ParamName != null
                ? firstValueParam.IsValueType
                    ? $"{firstValueParam.ParamName}.ToString()"
                    : $"{firstValueParam.ParamName}?.ToString()"
                : "null";

            var callArgs = string.Join(", ", tm.Parameters.Select(p => p.ParamName));
            var awaitPrefix = tm.ReturnsTask ? "await " : "";
            var callSite = isStatic ? $"{typeShortName}.{tm.MethodName}" : $"_self.{tm.MethodName}";
            var valueToString = tm.ResultValueIsValueType ? "result.Value.ToString()" : "result.Value?.ToString()";
            var valueExpr = tm.ResultIsGeneric
                ? $"result.IsSuccess ? {valueToString} : null"
                : "null";
            // DebugProxy accesses _nodeIds_ via enclosing FlowProxy class name
            var nodeIdsRef = isDebug
                ? $"FlowProxy._nodeIds_{tm.MethodName}"
                : $"_nodeIds_{tm.MethodName}";

            if (isDebug)
            {
                sb.AppendLine($"{indent}    var buf = new global::REslava.Result.Observers.RingBufferObserver(capacity: 1);");
                sb.AppendLine($"{indent}    global::REslava.Result.PipelineObserver.Register(buf);");
            }

            sb.AppendLine($"{indent}    var scope = global::REslava.Result.PipelineObserver.BeginPipeline(");
            sb.AppendLine($"{indent}        \"{EscapeStringLiteral(tm.PipelineId)}\",");
            sb.AppendLine($"{indent}        \"{EscapeStringLiteral(tm.MethodName)}\",");
            sb.AppendLine($"{indent}        {inputValueExpr},");
            sb.AppendLine($"{indent}        {nodeIdsRef});");
            sb.AppendLine($"{indent}    try");
            sb.AppendLine($"{indent}    {{");
            sb.AppendLine($"{indent}        var result = {awaitPrefix}{callSite}({callArgs});");
            sb.AppendLine($"{indent}        scope.End(");
            sb.AppendLine($"{indent}            result.IsSuccess,");
            sb.AppendLine($"{indent}            {valueExpr},");
            sb.AppendLine($"{indent}            result.IsFailure && result.Errors.Count > 0 ? result.Errors[0].GetType().Name : null);");
            sb.AppendLine($"{indent}        return result;");
            sb.AppendLine($"{indent}    }}");

            if (isDebug)
            {
                sb.AppendLine($"{indent}    finally");
                sb.AppendLine($"{indent}    {{");
                sb.AppendLine($"{indent}        scope.Dispose();");
                sb.AppendLine($"{indent}        global::REslava.Result.PipelineObserver.Unregister();");
                sb.AppendLine($"{indent}        buf.Save(\"debug-{EscapeStringLiteral(tm.MethodName)}.json\");");
                sb.AppendLine($"{indent}    }}");
            }
            else
            {
                sb.AppendLine($"{indent}    finally {{ scope.Dispose(); }}");
            }

            sb.AppendLine($"{indent}}}");
            sb.AppendLine();
        }

        private static string EscapeStringLiteral(string s) => s.Replace("\\", "\\\\").Replace("\"", "\\\"");

        private static string BuildLegendMermaid()
        {
            var sb = new StringBuilder();
            sb.AppendLine("flowchart LR");
            // S[ ]:::entry is invisible; ==> shows the thick entry arrow into Entry node.
            // ~~~ links are invisible (no line/arrow) and force horizontal LR placement.
            sb.AppendLine("    S[ ]:::entry ==> E[\"Entry\"]:::operation");
            sb.AppendLine("    E ~~~ B[\"Bind\"]:::bind");
            sb.AppendLine("    B ~~~ M[\"Map\"]:::map");
            sb.AppendLine("    M ~~~ G[\"<span title='hover shows condition'>Guard</span>\"]:::gatekeeper");
            sb.AppendLine("    G ~~~ T[\"Tap\"]:::sideeffect");
            sb.AppendLine("    T ~~~ X{{\"Match\"}}:::terminal");
            sb.AppendLine("    X ~~~ SUC([success]):::success");
            sb.AppendLine("    X ~~~ F([fail]):::failure");
            sb.AppendLine("    F ~~~ A[\"\u26a1 = async \u00b7 Guard: condition on hover<br/>FAIL: 1\u20133 errors inline \u00b7 4+ as \u2139\uFE0F tooltip\"]:::note");
            sb.AppendLine("    A ~~~ V[\"_Diagram \u00b7 _TypeFlow (edge type labels) \u00b7 _LayerView \u00b7 _Stats \u00b7 _ErrorSurface \u00b7 _ErrorPropagation\"]:::note");
            sb.AppendLine("    classDef entry      fill:none,stroke:none");
            sb.AppendLine("    classDef operation  fill:#fef0e3,color:#b86a1c");
            sb.AppendLine("    classDef bind       fill:#e3f0e8,color:#2f7a5c,stroke:#1a5c3c,stroke-width:3px");
            sb.AppendLine("    classDef map        fill:#e3f0e8,color:#2f7a5c");
            sb.AppendLine("    classDef gatekeeper fill:#e3e9fa,color:#3f5c9a");
            sb.AppendLine("    classDef sideeffect fill:#fff4d9,color:#b8882c");
            sb.AppendLine("    classDef terminal   fill:#f2e3f5,color:#8a4f9e");
            sb.AppendLine("    classDef success    fill:#e6f6ea,color:#1c7e4f");
            sb.AppendLine("    classDef failure    fill:#f8e3e3,color:#b13e3e");
            sb.AppendLine("    classDef note       fill:#f5f5f5,color:#555555,stroke:#cccccc");
            return sb.ToString().TrimEnd();
        }
    }
