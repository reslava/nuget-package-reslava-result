using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;
using REslava.Result.Flow.Generators.ResultFlow.Models;
using REslava.Result.Flow.Generators.ResultFlow.Orchestration;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace REslava.Result.Flow.Generators.ResultFlow.CodeGeneration
{
    internal static class ResultFlowRegistryCodeGenerator
    {
        /// <summary>
        /// Generates <c>{ClassName}_PipelineRegistry.g.cs</c> with a <c>_Methods</c> JSON index
        /// and a <c>{MethodName}_Info</c> JSON constant per method.
        /// </summary>
        public static SourceText Generate(
            string className,
            IReadOnlyList<MethodRegistryModel> methods,
            Compilation compilation,
            INamedTypeSymbol? resultBaseSymbol,
            INamedTypeSymbol? iErrorSymbol)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// REslava.Result.Flow — pipeline registry");
            sb.AppendLine("// Do not edit manually.");
            sb.AppendLine();
            sb.AppendLine("namespace Generated.ResultFlow");
            sb.AppendLine("{");
            sb.AppendLine($"    internal static class {className}_PipelineRegistry");
            sb.AppendLine("    {");

            // _Methods index
            var methodNames = string.Join(",", methods.Select(m => $"\\\"{m.MethodName}\\\""));
            sb.AppendLine($"        public const string _Methods = \"[{methodNames}]\";");
            sb.AppendLine();

            foreach (var method in methods)
            {
                string infoJson;
                if (method.HasDiagram && method.Syntax != null && resultBaseSymbol != null && iErrorSymbol != null)
                {
                    // Full metadata: run chain extractor
                    var semanticModel = compilation.GetSemanticModel(method.Syntax.SyntaxTree);
                    var chain = ResultFlowChainExtractor.Extract(
                        method.Syntax,
                        semanticModel,
                        compilation,
                        resultBaseSymbol,
                        iErrorSymbol,
                        maxDepth: method.MaxDepth);

                    if (chain != null)
                    {
                        var visibleNodes = chain.Where(n => n.Kind != NodeKind.Invisible).ToList();
                        var nodeCount    = visibleNodes.Count;
                        var chainErrors  = chain.SelectMany(n => CollectErrors(n));
                        // Depth-2 body scan: catches errors in seed methods and Match-terminal
                        // pipelines where the chain walker cannot reach the errors.
                        var bodyErrors   = ScanBodyForErrors(
                            method.Syntax,
                            semanticModel,
                            compilation,
                            iErrorSymbol,
                            remainingDepth: 2,
                            visited: new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal));
                        var errorTypes   = chainErrors.Concat(bodyErrors).Distinct().ToList();
                        var kindFlags    = visibleNodes
                            .Select(n => KindToFlag(n.Kind))
                            .Where(f => f != null)
                            .Distinct()
                            .ToList();
                        var nodeIds = visibleNodes
                            .Select((n, i) => ShortHash.Compute(method.PipelineId, n.MethodName, i.ToString()))
                            .ToList();

                        infoJson = BuildFullInfo(method, nodeCount, errorTypes, kindFlags!, nodeIds);
                    }
                    else
                    {
                        // [ResultFlow] present but chain extraction failed — emit hasDiagram:true, no counts
                        infoJson = BuildMinimalInfo(method, hasDiagram: true);
                    }
                }
                else
                {
                    infoJson = BuildMinimalInfo(method, hasDiagram: false);
                }

                var escaped = infoJson.Replace("\\", "\\\\").Replace("\"", "\\\"");
                sb.AppendLine($"        /// <summary>Pipeline registry info for {method.MethodName}.</summary>");
                sb.AppendLine($"        public const string {method.MethodName}_Info = \"{escaped.Trim()}\";");
                sb.AppendLine();
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");

            return SourceText.From(sb.ToString(), Encoding.UTF8);
        }

        // ── JSON builders ─────────────────────────────────────────────────────

        private static string BuildFullInfo(
            MethodRegistryModel method,
            int nodeCount,
            IReadOnlyList<string> errorTypes,
            IReadOnlyList<string> kindFlags,
            IReadOnlyList<string> nodeIds)
        {
            var errorArr  = ToJsonArray(errorTypes);
            var kindArr   = ToJsonArray(kindFlags);
            var nodeIdArr = ToJsonArray(nodeIds);
            return
                $"{{\"pipelineId\":\"{method.PipelineId}\"," +
                $"\"namespace\":\"{EscapeJson(method.Namespace)}\"," +
                $"\"sourceLine\":{method.SourceLine}," +
                $"\"returnType\":\"{method.ReturnType}\"," +
                $"\"returnTypeFullName\":\"{EscapeJson(method.ReturnTypeFullName)}\"," +
                $"\"isAsync\":{BoolStr(method.IsAsync)}," +
                $"\"hasDiagram\":true," +
                $"\"nodeCount\":{nodeCount}," +
                $"\"nodeIds\":{nodeIdArr}," +
                $"\"errorTypes\":{errorArr}," +
                $"\"nodeKindFlags\":{kindArr}}}";
        }

        private static string BuildMinimalInfo(MethodRegistryModel method, bool hasDiagram)
        {
            return
                $"{{\"pipelineId\":\"{method.PipelineId}\"," +
                $"\"namespace\":\"{EscapeJson(method.Namespace)}\"," +
                $"\"sourceLine\":{method.SourceLine}," +
                $"\"returnType\":\"{method.ReturnType}\"," +
                $"\"returnTypeFullName\":\"{EscapeJson(method.ReturnTypeFullName)}\"," +
                $"\"isAsync\":{BoolStr(method.IsAsync)}," +
                $"\"hasDiagram\":{BoolStr(hasDiagram)}}}";
        }

        // ── Helpers ───────────────────────────────────────────────────────────

        /// <summary>
        /// Collects all PossibleErrors from a node and its sub-nodes recursively.
        /// </summary>
        private static IEnumerable<string> CollectErrors(PipelineNode node)
        {
            foreach (var e in node.PossibleErrors)
                yield return e;
            if (node.SubNodes != null)
                foreach (var sub in node.SubNodes)
                    foreach (var e in CollectErrors(sub))
                        yield return e;
        }

        /// <summary>
        /// Depth-2 body scan: finds all <c>new XxxError(...)</c> instantiations where the type
        /// implements <c>IError</c>, then recurses into called methods up to
        /// <paramref name="remainingDepth"/> levels. Catches errors in seed methods and
        /// Match-terminal pipelines that the chain walker cannot reach.
        /// </summary>
        private static IEnumerable<string> ScanBodyForErrors(
            MethodDeclarationSyntax method,
            SemanticModel semanticModel,
            Compilation compilation,
            INamedTypeSymbol iErrorSymbol,
            int remainingDepth,
            System.Collections.Generic.HashSet<string> visited)
        {
            // Use file path + method name as cycle-guard key
            var key = method.SyntaxTree.FilePath + "|" + method.Identifier.ValueText;
            if (!visited.Add(key)) yield break;

            // Scan for `new XxxError(...)` whose type implements IError
            foreach (var creation in method.DescendantNodes().OfType<ObjectCreationExpressionSyntax>())
            {
                var typeSymbol = semanticModel.GetTypeInfo(creation).Type as INamedTypeSymbol;
                if (typeSymbol != null && ResultTypeExtractor.ImplementsInterface(typeSymbol, iErrorSymbol))
                    yield return typeSymbol.Name;
            }

            if (remainingDepth <= 0) yield break;

            // Descend into called methods that have a local source declaration
            foreach (var invocation in method.DescendantNodes().OfType<InvocationExpressionSyntax>())
            {
                if (!(semanticModel.GetSymbolInfo(invocation).Symbol is IMethodSymbol calledSymbol)) continue;
                var syntaxRef = calledSymbol.DeclaringSyntaxReferences.FirstOrDefault();
                if (syntaxRef == null) continue;
                var calledDecl = syntaxRef.GetSyntax() as MethodDeclarationSyntax;
                if (calledDecl == null) continue;
                var calledModel = compilation.GetSemanticModel(calledDecl.SyntaxTree);
                foreach (var e in ScanBodyForErrors(calledDecl, calledModel, compilation, iErrorSymbol, remainingDepth - 1, visited))
                    yield return e;
            }
        }

        /// <summary>
        /// Maps internal <see cref="NodeKind"/> to the user-facing flag string stored in
        /// <c>nodeKindFlags</c>. Returns null for kinds that are not surfaced (Invisible).
        /// </summary>
        private static string? KindToFlag(NodeKind kind)
        {
            return kind switch
            {
                NodeKind.Unknown            => "entry",
                NodeKind.TransformWithRisk  => "Bind",
                NodeKind.PureTransform      => "Map",
                NodeKind.SideEffectSuccess  => "Tap",
                NodeKind.SideEffectFailure  => "TapOnFailure",
                NodeKind.SideEffectBoth     => "TapBoth",
                NodeKind.Gatekeeper         => "Gatekeeper",
                NodeKind.Terminal           => "Terminal",
                NodeKind.Invisible          => null,
                _                           => null,
            };
        }

        private static string ToJsonArray(IReadOnlyList<string> items)
        {
            if (items.Count == 0) return "[]";
            return "[" + string.Join(",", items.Select(s => $"\"{EscapeJson(s)}\"")) + "]";
        }

        private static string BoolStr(bool value) => value ? "true" : "false";

        private static string EscapeJson(string value) =>
            value.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }
}
