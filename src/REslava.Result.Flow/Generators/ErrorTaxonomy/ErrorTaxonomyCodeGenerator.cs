using Microsoft.CodeAnalysis.Text;
using System.Text;

namespace REslava.Result.Flow.Generators.ErrorTaxonomy
{
    /// <summary>
    /// Emits <c>{Class}_ErrorTaxonomy.g.cs</c> containing a single
    /// <c>Generated.ErrorTaxonomy.{Class}_ErrorTaxonomy</c> static class with an
    /// <c>_ErrorTaxonomy</c> string constant (markdown table).
    /// </summary>
    internal static class ErrorTaxonomyCodeGenerator
    {
        public static SourceText Generate(string className, string markdownTable)
        {
            var escapedTable = markdownTable.Replace("\"", "\"\"");

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// REslava.Result.Flow — auto-generated error taxonomy");
            sb.AppendLine("// Do not edit manually.");
            sb.AppendLine();
            sb.AppendLine("namespace Generated.ErrorTaxonomy");
            sb.AppendLine("{");
            sb.AppendLine($"    /// <summary>Error taxonomy for {className} — generated by REslava.Result.Flow</summary>");
            sb.AppendLine($"    internal static class {className}_ErrorTaxonomy");
            sb.AppendLine("    {");
            sb.AppendLine("        /// <summary>");
            sb.AppendLine($"        /// Markdown table of all detectable error types in {className}.");
            sb.AppendLine("        /// Columns: Method | Error Type | Confidence (certain = from return type; inferred = from body scan)");
            sb.AppendLine("        /// </summary>");
            sb.Append($"        public const string _ErrorTaxonomy = @\"{escapedTable}");
            sb.AppendLine("\";");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            return SourceText.From(sb.ToString(), Encoding.UTF8);
        }
    }
}
