using Microsoft.CodeAnalysis.Text;
using REslava.Result.SourceGenerators.Core.Interfaces;
using System.Text;

namespace REslava.Result.SourceGenerators.Generators.OneOf2ToIResult.Attributes
{
    /// <summary>
    /// Generates the MapToProblemDetails attribute.
    /// Single Responsibility: Only generates this specific attribute.
    /// </summary>
    public class MapToProblemDetailsAttributeGenerator : IAttributeGenerator
    {
        public SourceText GenerateAttribute()
        {
            var source = @"
using System;

namespace REslava.Result.SourceGenerators.OneOf2
{
    /// <summary>
    /// Maps error types to HTTP status codes for OneOf&lt;T1,T2&gt; to IResult conversion.
    /// Generated by REslava.Result.SourceGenerators for OneOf2ToIResult.
    /// </summary>
    [AttributeUsage(AttributeTargets.Class | AttributeTargets.Struct, AllowMultiple = true)]
    public class MapToProblemDetailsAttribute : Attribute
    {
        /// <summary>
        /// Gets or sets the HTTP status code to return when this error type is encountered.
        /// </summary>
        public int StatusCode { get; set; }

        /// <summary>
        /// Gets or sets the custom title for the ProblemDetails.
        /// </summary>
        public string? Title { get; set; }

        /// <summary>
        /// Gets or sets the custom detail for the ProblemDetails.
        /// </summary>
        public string? Detail { get; set; }

        /// <summary>
        /// Gets or sets the custom type for the ProblemDetails.
        /// </summary>
        public string? Type { get; set; }
    }
}";

            return SourceText.From(source, Encoding.UTF8);
        }
    }
}
